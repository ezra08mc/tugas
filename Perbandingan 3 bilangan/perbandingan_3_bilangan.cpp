#include <iostream>
using namespace std;

int main() {
    int A, B, C, max;
    cout << "Masukkan bilangan A: ";
    cin >> A;
    cout << "Masukkan bilangan B: ";
    cin >> B;
    cout << "Masukkan bilangan C: ";
    cin >> C;

    max = A;
    if (B > max) max = B;
    if (C > max) max = C;

    cout << "Bilangan terbesar adalah: " << max << endl;

    return 0;
}
