#include <iostream>
using namespace std;

int main() {
    float X, Y;

    cout << "Masukkan koordinat X: ";
    cin >> X;
    cout << "Masukkan koordinat Y: ";
    cin >> Y;

    if (X > 0 && Y > 0) {
        cout << "Titik (" << X << ", " << Y << ") berada di Kuadran I.\n";
    } else if (X < 0 && Y > 0) {
        cout << "Titik (" << X << ", " << Y << ") berada di Kuadran II.\n";
    } else if (X < 0 && Y < 0) {
        cout << "Titik (" << X << ", " << Y << ") berada di Kuadran III.\n";
    } else if (X > 0 && Y < 0) {
        cout << "Titik (" << X << ", " << Y << ") berada di Kuadran IV.\n";
    } else {
        cout << "Titik (" << X << ", " << Y << ") berada di salah satu sumbu X atau Y.\n";
    }

    return 0;
}
